const producto = {
    nombre: "Monitor 20 pulgadas",
    precio: 300,
    disponible: true,
  };

// Object methods Keys
console.log(Object.keys(producto)); //Obtiene las llaves o variables del objeto (parte izquierda)

// Object methods values
console.log(Object.values(producto)); // retorna los valores del objeto (parte derecha)

// Object methods entries
console.log(Object.entries(producto));// retorna todo el pares (variable y valor de la variable)