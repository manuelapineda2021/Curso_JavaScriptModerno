// Incrementos o Decrementos
let puntaje= 5;

// Incrementos
// Se ejecuta puntaje y despues se incrementa
puntaje++; //5

// se incrementa y se ejecuta el valor de puntaje
++puntaje; //7

// Decrementos (se comporta igual al incremento)
puntaje--;

--puntaje;

// No incrementando de uno en uno
puntaje +=3;
puntaje +=6;
puntaje +=2;

// No decrementa de uno en uno
puntaje -=3;
puntaje -=4;
puntaje -=6;

console.log(puntaje);